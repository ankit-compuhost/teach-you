      <td>Students from Class 3 to 8 of Delhi government and government-aided schools will not have to sit for offline exams and will instead be graded on the basis of projects and assignments, the Delhi government said Wednesday. The government has issued guidelines in this regard, which are applicable for the 2020-21 academic session, when schools were closed due to the COVID-19 pandemic and all teaching and learning activities were conducted online. "Since no classroom teaching and learning has taken place at the primary and middle level, the formal mode of pen and paper assessment will be replaced by a subject wise assessment of projects and assignments for Classes 3 to 8," said Rita Sharma, Additional Director of Education, Delhi. Schools were reopened in Delhi for students of Class 9 and Class 11 on February 5. For classes 10 and 12, classes had resumed on January 18 in light of the board exams. According to the guidelines, for Classes 3 to 5, 30 marks will be for assessment based on worksheets, 30 for the assignments given in winter breaks and 40 marks for the assignments and projects provided from March 1 to 15. Similarly, for Classes 6 to 8, 20 marks will be for assessment based on worksheets, 30 for the assignments given in winter breaks and 50 marks for the assignments and projects provided from March 1 to 15. "In case a student does not have access to a digital device or internet, the assignments and projects will be given in hard copies by calling his parents to the school while following the COVID-19 guidelines," the guidelines state. In a separate circular Wednesday, the Delhi government said students from classes KG to 2 will be assessed in the month of March based on assignments given during the winter break and "responses to online/ offline worksheets shared with the students during COVID- 19 Pandemic through digital modes/ WhatsApp/ in person by the parents/ guardians". "All the currently enrolled students of classes Nursery to II shall be promoted to the next class in the Academic Session 2021-22. However, the purpose of this assessment is to understand the impact of alternate learning approach that needed to be adopted under the prevailing circumstances this year," DDE (Primary) Harsh Arya said.</td>
