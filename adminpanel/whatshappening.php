      <td>The Directorate of School Education Jammu (DSEJ) has launched the Tamanna aptitude test for students of government schools in the region to help them make appropriate academic choices and career-related decisions, officials said on Wednesday. The DSEJ initiated the aptitude testing process from Tuesday, School Education Jammu Director Anuradha Gupta told reporters here. "To begin with, the Counselling Cell of the DSEJ conducted two days online orientation cum training programme of faculty counsellors of Jammu division from winter zone, in which 500 faculty counsellors and psychological task force members were trained for conducting the test on the students," she said. More such training sessions will be conducted before the actual test, which will be in offline mode, Gupta said. The 'Tamanna' (Try and Measure Aptitude and Natural Abilities) test has been developed for senior school students by the National Council of Educational Research and Training (NCERT), New Delhi, under the aegis of the Union Ministry of Education. "The test contains items on seven dimensions measured in aptitude testing i.e., Language Aptitude (LA), Abstract Reasoning (AR), Verbal Reasoning (VR), Mechanical Reasoning (MR), Numerical Reasoning (NA), Spatial Aptitude (SA), and Perceptual Aptitude (PA), along with technical manual thereby, helping the students of Class 9 and 10 make a better choice of subjects," she said. The official said the DSEJ has taken the lead by being the first body to help students take the benefit of this test. As the test is in its initial phase, the Counselling Cell has been asked to make proper reports after conducting the test so as to give necessary inputs to the agency concerned for its further standardisation if required, she said. Winter zone schools are opening from March 1 and capacity-development of teachers is being done to mentor and guide the students. "In the new session, this test will be used as a tool to facilitate students for self-awarenes